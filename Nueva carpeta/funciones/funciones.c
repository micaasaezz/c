
int sumar(int a,int b)
{
    int resultado;
    resultado=a+b;
    return resultado;
}

int restar(int a, int b)
{
    int resultado;
    resultado=a-b;
    return resultado;
}

int multiplicar(int a,int b)
{
    int resultado;
    resultado=a*b;
    return resultado;
}

float dividir(int a,int b)
{
    float resultado;
    resultado = a/b;
    return resultado;
}
